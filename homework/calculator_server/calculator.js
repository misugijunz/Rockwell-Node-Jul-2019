const calculator = (function() {
    return {
      add: (n1, n2) => {
        result = n1 + n2;
        return result;
      },
      subtract: (n1, n2) => {
        result = n1 - n2;
        return result;
      },
      multiply: (n1, n2) => {
        result = n1 / n2;
        return result;
      },
      divide: (n1, n2) => {
        result = n1 / n2;
        return result;
      },
    };
  })();
  module.exports = calculator
